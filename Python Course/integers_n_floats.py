#with integers and floats we can store numeric data types in python
#we can also perform mathematical operrations

#integers are whole numbers (without decimal points but can  be positive or negative)
my_int_1 = 56
my_int_2 = -4

print(type(my_int_1)) # <class 'int'>
print(type(my_int_2)) # <class 'int'>

#how to perform addition with integers
my_int_1 = 56
my_int_2 = 12

#addition
sum_ints = my_int_1 + my_int_2
print('Integer Addition: ', sum_ints) #Integer addition: 68

#how to perform subtraction with integers

my_int_1 = 56
my_int_2 = 12

# Subtraction
diff_ints = my_int_1 - my_int_2
print('Integer Subtraction:', diff_ints) # Integer Subtraction: 44

